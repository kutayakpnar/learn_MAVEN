package org.kutaydavon;

public class MyMath {

    public int addition(int num1,int num2){
        return num1+num2;

    }
    public int substraction(int num1,int num2){
        return num1-num2;
    }

    public int multiplication(int num1,int num2){
        return num1*num2;
    }
    public float division(int num1,int num2){
        return num1/num2;

    }
}
